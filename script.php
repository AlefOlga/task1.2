<?php

$x = $_GET['x'] ?? rand(1, 100);
$f1 = 1;
$f2 = 1;

echo 'Число '.$x.'<br>'.PHP_EOL;

while (true) {
    if ($f1 > $x) {
        echo 'задуманное число не входит в числовой ряд';
        break;
    }
    if ($f1 == $x) {
        echo 'задуманное число входит в числовой ряд';
        break;
    }
    $f3 = $f1;
    $f1 = $f1 + $f2;
    $f2 = $f3;
}
